// Queremos hacer un programa que muestre los numeros del 1 a 10

console.log('EJERCICIO USANDO FOR')

for (let numero = 1; numero <= 10; numero++) {
   // Simepre que numero <= 10
   // este codigo se va a ejecutar
   console.log('Numero', numero);
}


